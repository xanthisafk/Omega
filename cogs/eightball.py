import random
import asyncio

import APIs.color as rang
import discord
from discord.ext import commands


class EightBall(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    ball8 = [
    "游릭 It is Certain.",
    "游릭 It is decidedly so.", 
    "游릭 Without a doubt", 
    "游릭 Yes definitely", 
    "游릭 You may rely on it", 
    "游릭 As I  see, yes", 
    "游릭 Most Likely", 
    "游릭 Outlook good", 
    "游릭 Yes", 
    "游릭 Signs points to yes", 
    "游리 Reply hazy, try again", 
    "游리 Ask again later", 
    "游리 Better not tell you now", 
    "游리 Cannot predict now", 
    "游리 Concentrate and ask again", 
    "游댮 Don't count on it", 
    "游댮 My reply is no",
    "游댮 My sources say no",
    "游댮 Outlook not so good",
    "游댮 Very doubtful" 
    ]

    @commands.command(name='8ball')
    async def ball_8(self, ctx, * , question :str = None) -> None:

        if question is None:
            await ctx.send("You need to ask a question for 游꿤 to work.")
            return

        color = await rang.get_color()

        desc = {
            1:"游꿤 Asking 8Ball...",
            2:"And... 游꿤 says..",
            3:"And 游꿤 says: "
        }

        embed = discord.Embed(title = f'You asked: "{question}"',description = (desc[1]), color = color)
        message = await ctx.send(embed=embed)
        await asyncio.sleep(random.randint(1,3))

        embed = discord.Embed(title = f'You asked: "{question}"',description = (desc[2]), color = color)
        await message.edit(embed=embed)
        await asyncio.sleep(random.randint(1,3))
        
        embed = discord.Embed(title = f'You asked: "{question}"',description = (desc[3]+random.choice(self.ball8)), color = color)
        await message.edit(embed=embed)


def setup(bot):
    bot.add_cog(EightBall(bot))
